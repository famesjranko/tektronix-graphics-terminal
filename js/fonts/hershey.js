// Lowercase letters (simplified - same as uppercase but smaller)
// For simplicity, map lowercase to uppercase
const lowercaseMap = 'abcdefghijklmnopqrstuvwxyz';
for (const char of lowercaseMap) {
  const upper = char.toUpperCase();
  if (HERSHEY_FONT[upper]) {
    HERSHEY_FONT[char] = HERSHEY_FONT[upper];
  }
}

/**
 * Get character data, falling back to a placeholder for unknown chars
 * @param {string} char - Single character
 * @returns {Object} Character data with width and strokes
 */
export function getCharData(char) {
  if (HERSHEY_FONT[char]) {
    return HERSHEY_FONT[char];
  }
  // Return a placeholder box for unknown characters
  return {
    width: 0.5,
    strokes: [
      [0, 0, 0.5, 0],
      [0.5, 0, 0.5, 1],
      [0.5, 1, 0, 1],
      [0, 1, 0, 0]
    ]
  };
}

/**
 * Calculate total width of a text string
 * @param {string} text - Text to measure
 * @param {number} spacing - Space between characters (default 0.1)
 * @returns {number} Total width in normalized units
 */
export function measureText(text, spacing = 0.1) {
  let width = 0;
  for (let i = 0; i < text.length; i++) {
    const charData = getCharData(text[i]);
    width += charData.width;
    if (i < text.length - 1) {
      width += spacing;
    }
  }
  return width;
}

/**
 * Generate line commands for rendering text
 * @param {string} text - Text to render
 * @param {number} x - Start X position (normalized 0-1)
 * @param {number} y - Baseline Y position (normalized 0-1)
 * @param {number} scale - Scale factor (1 = height matches ~0.03 of canvas)
 * @param {number} spacing - Character spacing (default 0.1)
 * @returns {Array} Array of line commands {type: 'line', x1, y1, x2, y2}
 */
export function textToCommands(text, x, y, scale = 1, spacing = 0.1) {
  const commands = [];
  const baseHeight = 0.03; // Base height in normalized coords
  const actualScale = baseHeight * scale;

  let cursorX = x;

  for (const char of text) {
    const charData = getCharData(char);

    for (const stroke of charData.strokes) {
      const [sx1, sy1, sx2, sy2] = stroke;
      commands.push({
        type: 'line',
        x1: cursorX + sx1 * actualScale,
        y1: y + sy1 * actualScale,
        x2: cursorX + sx2 * actualScale,
        y2: y + sy2 * actualScale
      });
    }

    cursorX += (charData.width + spacing) * actualScale;
  }

  return commands;
}
